#include "3-calc.h"

/**
 * op_add - add two integers
 * @a: first num
 * @b: second num
 *
 * Return: integer
 */
int op_add(int a, int b)
{
	return (a + b);
}


/**
 * op_sub - substract two integers
 * @a: first num
 * @b: second num
 *
 * Return: integer
 */
int op_sub(int a, int b)
{
	return (a - b);
}


/**
 * op_mul - multiply two integers
 * @a: first num
 * @b: second num
 *
 * Return: integer
 */
int op_mul(int a, int b)
{
	return (a * b);
}


/**
 * op_div - divide two integers
 * @a: first num
 * @b: second num
 *
 * Return: integer
 */
int op_div(int a, int b)
{
	return (a / b);
}



/**
 * op_mod - add two integers
 * @a: first num
 * @b: second num
 *
 * Return: integer
 */
int op_mod(int a, int b)
{
	return (a % b);
}

